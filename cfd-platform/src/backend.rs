fn reserve_loopback_port() -> Result<u16> {
    let listener = std::net::TcpListener::bind("127.0.0.1:0")
        .context("Unable to reserve a private loopback port for the backend")?;
    let port = listener
        .local_addr()
        .context("Unable to inspect the private loopback port")?
        .port();
    drop(listener);
    Ok(port)
}

/// Build a SQLite URL that remains valid when the desktop changes its working
/// directory. SQLAlchemy accepts Windows drive paths when their separators are
/// normalized to forward slashes.
fn local_sqlite_database_url(data_dir: &Path) -> Result<String> {
    std::fs::create_dir_all(data_dir).with_context(|| {
        format!(
            "Unable to create the local backend data directory: {}",
            data_dir.display()
        )
    })?;
    let database_path = data_dir.join("cfd.db");
    let url_path = database_path.to_string_lossy().replace('\\', "/");
    Ok(format!("sqlite+aiosqlite:///{}", url_path))
}
