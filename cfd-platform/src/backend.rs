    /// Check backend health via the private loopback endpoint selected at launch.
    pub async fn health_check(&self) -> Result<bool> {
        Ok(self.probe_health(Duration::from_secs(5)).await)
    }

    /// Probe the private FastAPI endpoint without exposing it to the webview.
    async fn probe_health(&self, request_timeout: Duration) -> bool {
        let endpoint = match self.endpoint.read().await.clone() {
            Some(endpoint) => endpoint,
            None => return false,
        };
        let client = reqwest::Client::new();
        let response = client
            .get(format!("{}/health", endpoint))
            .timeout(request_timeout)
            .send()
            .await;

        match response {
            Ok(resp) => resp.status().is_success(),
            Err(_) => false,
        }
    }
